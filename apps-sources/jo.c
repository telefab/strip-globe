/*
This demonstrator displays the image on the globe and makes it rotate slowly
*/

int main(int args, char *argv[])
{
	// Variables used
    int fd, fc, fs;
	int i, j, k = 0;
	int tt = 0;
    unsigned int *fb;
    char cntrl[1];	
	char speed[1];
	
	// 35 is a good speed for the globe
	speed[0] = 35;
	
	// Opening and sending speed to the driver
	fs = open("/dev/speed",O_RDWR);
	write(fs, &speed, 1);
	
	// Opening the memory
    fd = open ("/dev/mem", O_RDWR);
    if (fd < 1) {
        perror("erreur ouverture /dev/mem\n");
        return -1;
    }

	// Mapping the frame buffer
    fb = mmap(NULL, FB_SIZE, PROT_READ|PROT_WRITE, MAP_SHARED, fd, FB_ADDR);
    if  (fb == NULL) {
        printf("Error while mapping framebuffer\n");
        return -1;
    }
    	
	// Opening the control driver
    fc = open("/dev/cntrl", O_RDWR);
    if (fc < 1) {
        perror("erreur ouverture /dev/mem\n");
        return -1;
    }

	// WR1 : Writing in the frame buffer 1
	// PS_CONTROL : The cntrl says which frame buffer reads the display part of the globe, here the frame buffer 0
    cntrl[0] = WR1 | PS_CONTROL;
	
	// Sending the value to the drivers
    write(fc, &cntrl, 1);
	
	// Waiting until the globe has reached the given speed
	for(tt = 0; tt < 100000000;tt++);

	// Loop until the image has been shifted 300 times
	while(k < 300)
	{
		// Pause during the step time
		for(tt = 0; tt < 10000000;tt++);
		
		// Sending the image to the frame buffer
		for(i = 0;i < 100;i++) 
		{
			for(j = 0;j < 49;j++)
			{
				// Write the shifted image
				*(fb + (i*49 + j)) = image[48-j][(i+k) % 100];
			}
		}
		
		// Switch the read frame buffer and the written frame buffer
		if(k % 2 == 1)
		{
			cntrl[0] = WR1 | PS_CONTROL;
		}
		else
		{
			cntrl[0] = WR0 | PS_CONTROL | RAM_ENABLE;
		}
		write(fc, &cntrl, 1);
		
		k++;
	}
	
	
	// Put the control signals back to zero
    cntrl[0] = 0;
    write(fc, &cntrl, 1); 

	// Unmapping the frame buffer
    munmap(fb, FB_SIZE);
	
	// Closing the drivers
    close(fc);
    close(fd);
	close(fs);

    return 0;
}
